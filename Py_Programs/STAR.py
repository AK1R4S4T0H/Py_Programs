def stop_drawing():
    turtle.penup()
    turtle.hideturtle()
    turtle.done = True

def save_image():
    file_name = filedialog.asksaveasfilename(defaultextension=".png")
    if file_name:
        try:
            canvas.getcanvas().postscript(file="turtle.eps")
            os.system("convert turtle.eps " + file_name)
        except Exception as e:
            messagebox.showerror("Error", str(e))

